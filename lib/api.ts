export const ticketApi = {
  getAll: async (page = 1, limit = 10): Promise<any> => {
    try {
      const response = await api.get(`api/tickets?page=${page}&limit=${limit}`);
      const data = extractData(response) || [];
      const total = response.data?.total || data.length;
      return { data, total };
    } catch (e) {
      throw e;
    }
  },
  getById: async (id: string): Promise<any> => {
    try {
      const response = await api.get(`api/tickets/${id}`);
      return extractData(response);
    } catch (e) {
      throw e;
    }
  },
  create: async (ticketData: Partial<Ticket>): Promise<any> => {
    try {
      const response = await api.post("api/tickets", ticketData);
      return extractData(response);
    } catch (e) {
      throw e;
    }
  },
  respond: async (
    ticketId: string,
    actionData: {
      action: "ACKNOWLEDGE" | "CONTEST" | "RESOLVE" | "VOID";
      contest_note?: string;
      contest_image?: File;
    },
  ): Promise<any> => {
    try {
      // If there is an image, we need FormData
      if (actionData.contest_image) {
        const formData = new FormData();
        formData.append("action", actionData.action);
        if (actionData.contest_note)
          formData.append("contest_note", actionData.contest_note);
        formData.append("contest_image", actionData.contest_image);
        const response = await api.patch(
          `api/tickets/${ticketId}/respond`,
          formData,
          {
            headers: { "Content-Type": "multipart/form-data" },
          },
        );
        return extractData(response);
      }

      const response = await api.patch(
        `api/tickets/${ticketId}/respond`,
        actionData,
      );
      return extractData(response);
    } catch (e) {
      throw e;
    }
  },
  delete: async (id: string): Promise<any> => {
    try {
      const response = await api.delete(`api/tickets/${id}`);
      return extractData(response);
    } catch (e) {
      throw e;
    }
  },
};

